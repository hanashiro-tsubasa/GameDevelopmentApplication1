#include "Player.h"
#include "../../Utility/InputControl.h"
#include "DxLib.h"

Player::Player() : animation_count(0), flip_flag(FALSE)
{
	animation[0] = NULL;
	animation[1] = NULL;
}

Player::~Player()
{

}

//初期化処理
void Player::Initialize()
{
	serial_number = 1;
	//画像の読み込み
	animation[0] = LoadGraph("Resource/Images/Tri-pilot/1.png");
	animation[1] = LoadGraph("Resource/Images/Tri-pilot/2.png");

	//エラーチェック
	if (animation[0] == -1 || animation[1] == -1)
	{
		throw("鳥パイロットの画像がありません\n");
	}

	//向きの設定
	radian = 0.0f;

	//当たり判定の大きさを設定
	box_size = Vector2D(64.0f);

	//初期画像の設定
	image = animation[0];
}

//更新処理
void Player::Update()
{
	//移動処理
	Movement();

	//アニメーション制御
	AnimationControl();
}

//描画処理
void Player::Draw() const
//2
{
	//プレイヤー画像の描画
	DrawRotaGraphF(location.x, location.y, 0.7, radian, image, TRUE, flip_flag);
	
	//親クラスの描画処理を呼び出す
	__super::Draw();
}

//終了時処理
void Player::Finalize()
{
	//使用した画像を開放
	DeleteGraph(animation[0]);
	DeleteGraph(animation[1]);
}

//当たり判定通知処理
void Player::OnHitCollision(GameObject* hit_object)
{
	//当たった時の処理
}

//移動処理
void Player::Movement()
{
	//移動の速さ
	Vector2D velocity = 0.0f;

	//左右移動
	if ((InputControl::GetKey(KEY_INPUT_LEFT)|| InputControl::GetKey(KEY_INPUT_A))&&location.x>0)
	{
		velocity.x += -3.0f;
		flip_flag = TRUE;
	}
	else if ((InputControl::GetKey(KEY_INPUT_RIGHT)|| InputControl::GetKey(KEY_INPUT_D))&&location.x<640)
	{
		velocity.x += 3.0f;
		flip_flag = FALSE;
	}
	
	else
	{
		velocity.x = 0.0f;
	}
	//3
	//現在の位置座標に速さを加算する
	location += velocity;
}

//アニメーション制御
void Player::AnimationControl()
{
	//アニメーションカウントを加算する
	animation_count++;

	//15フレーム目に到達したら
	if (animation_count >=48)
	{
		//カウントのリセット
		animation_count = 0;

		//画像の切り替え
		if (image == animation[0])
		{
			image = animation[1];
		}
		else
		{
			image = animation[0];
		}
	}
}
